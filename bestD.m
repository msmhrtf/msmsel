s1 = 3.7948;
min = 100;
best = 0;

for i = 1:45
    s2 = D(i,1) + D(i,2) + D(i,4);
    temp = abs (s1 - s2);
    if (temp < min)
        min = temp;
        best = i;
    end
end
disp("Differenza = " + min)
disp("Cipic ID = " + id(best))